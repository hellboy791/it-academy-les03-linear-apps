public class FirstDocFirstExercise {
    public static void main(String[] args) {
        // 1. Даны два действительных числа х и у. Вычислить их сумму, разность, произведение и частное.
        int x = 35;
        int y = 7;
        int res;
        res = x + y;
        System.out.println("x + y = " + res);
        res = x - y;
        System.out.println("x - y = " + res);
        res = x * y;
        System.out.println("x * y = " + res);
        res = x / y;
        System.out.println("x / y = " + res);
    }
}
